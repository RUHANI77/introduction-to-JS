var numbers = [78, 45, 98, 45];
//use push to add element of an array in the last index.
numbers.push(66);
console.log(numbers);

//pop use to get last element

numbers.pop();
console.log(numbers);

//use unshift to add element of an array in the first index.

var a = [23, 45, 12, 67];
a.unshift(34);
console.log(a);

//shift use to get first element
var a = [23, 45, 12, 67];
a.shift(34);
console.log(a);
